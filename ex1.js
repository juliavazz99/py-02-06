const frutas = ['Uva', 'Morango', 'Banana'];

// 1. Imprimir o primeiro elemento
console.log(frutas[0]);  // Saída: 'Uva'

// 2. Imprimir o último elemento (usando array[array.length - 1])
console.log(frutas[frutas.length - 1]);  // Saída: 'Banana'

// 3. Imprimir o valor de array.length
console.log(frutas.length);  // Saída: 3

// 4. Acessar um índice que não existe (exemplo: índice 10)
console.log(frutas[10]);  // Saída: undefined
