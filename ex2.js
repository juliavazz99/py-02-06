// Passo 1: Criar um array com três elementos
let frutas = ['Maçã', 'Banana', 'Laranja'];

// Imprimir o array e seu tamanho
console.log(frutas);  // Saída: ['Maçã', 'Banana', 'Laranja']
console.log(frutas.length);  // Saída: 3

// Passo 2: Modificar um elemento
frutas[1] = 'Uva';  // Mudando 'Banana' para 'Uva'

// Passo 3: Imprimir o array e seu tamanho novamente
console.log(frutas);  // Saída: ['Maçã', 'Uva', 'Laranja']
console.log(frutas.length);  // Saída: 3
